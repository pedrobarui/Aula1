// SOMAR
const somar = function (numA, numB){
    return numA + numB
}

// SUBTRAIR
const subtrair = function (numA, numB){
    return numA - numB
}
//MULTIPLICAR
const multiplicar = function (numA, numB){
    return numA * numB
}
// DIVIDIR
const dividir = function (numA, numB){
    return numA / numB
}

//EXPORTANDO AS FUNCIONALIDADES

module.exports = {
    somar, subtrair, multiplicar, dividir
}
